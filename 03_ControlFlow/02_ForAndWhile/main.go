package main

import "fmt"

func main()  {
	//	For in Go
	for i := 0; i <= 10; i++ {
		fmt.Println(i)
	}

	//	While in Go
	i := 1
	for i <= 10 {
		fmt.Println("Farkhan Hamzah Firdaus %d", i)
		i++
	}

	//	Infinite loop in Go
	for {
		fmt.Println("Infinite Loop")
	}

	//	There is no do while in Go
}
